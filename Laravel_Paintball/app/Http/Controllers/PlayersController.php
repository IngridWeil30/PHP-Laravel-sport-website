    public function addPlayer(Request $request) {

        Players::create([
            'name' => $request['name'],
            'nickname' => $request['nickname'],
            'age' => ($request['age']),
            'team_id' => ($request['team_id'])
        ]);
        Session::flash('playerCreated', "Player created");
        return redirect("/admin/match");

    }
